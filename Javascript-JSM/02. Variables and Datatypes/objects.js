// Objects in javascript
// Objects are non - primitive data type in javascript

// Objects are used to store collection of data or a more complex entity
// Objects are used to group data

// const name = "John";
// const age = 18;

const person = {
    name: "John",
    age: 18
}
console.log(person);
console.log(typeof person);

// using the dot notation, we can access the properties/data of an object
console.log(person.name, person.age);
console.log(typeof person.name, typeof person.age);

// Array
const arr = [1, 2, 3, 4, 5];
// In an array we can store multiple primitive values

console.log(arr);
console.log(typeof arr);

const date = new Date();
console.log(date);
console.log(typeof date);